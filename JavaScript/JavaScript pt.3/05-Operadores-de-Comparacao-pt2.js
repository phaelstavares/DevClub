/*
    Operadores de comparação

    >   ->   Maior
    <   ->   Menor
    ==  ->   Igual

    >=  ->   Maior ou Igual
    <=  ->   Menor ou Igual
*/

const primeiroNumero = 20
const segundoNumero = 30
const terceiroNumero = 20

console.log(primeiroNumero > segundoNumero) // 20 é MAIOR que 30?

console.log(primeiroNumero < segundoNumero) // 20 é MENOR que 30?

console.log(primeiroNumero == terceiroNumero) // 20 é IGUAL a 20?

console.log(primeiroNumero >= terceiroNumero) // É IGUAL

console.log(primeiroNumero <= segundoNumero) // É MENOR